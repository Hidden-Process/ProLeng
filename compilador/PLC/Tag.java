public class Tag {
    
	private String v;
	private String f;
	
	public Tag(String y, String n){
		v = y;
		f = n;
	}

	public String getV(){
		return v;
	}
	
	public String getF(){
		return f;
	}

	public void setV(String c){
		v = c;
	}

	public void setF(String c){
		f = c;
	}
}
